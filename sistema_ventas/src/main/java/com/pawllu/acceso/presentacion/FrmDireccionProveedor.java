    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (tabla.getSelectedRowCount() == 1) {//boton editar

            //carga la información de la tabla a los campos de texto
            String rut = String.valueOf(tabla.getValueAt(tabla.getSelectedRow(), 0));
            rutAnterior = Integer.parseInt(rut);
            String nombreProveedor = String.valueOf(tabla.getValueAt(tabla.getSelectedRow(), 1));
            String calle = String.valueOf(tabla.getValueAt(tabla.getSelectedRow(), 2));
            String numero = String.valueOf(tabla.getValueAt(tabla.getSelectedRow(), 3));
            String ciudad = String.valueOf(tabla.getValueAt(tabla.getSelectedRow(), 4));
            String comuna = String.valueOf(tabla.getValueAt(tabla.getSelectedRow(), 5));

            txtCalle.setText(calle);
            txtNumero.setText(numero);
            txtCiudad.setText(ciudad);
            txtComuna.setText(comuna);

            this.cboProveedor.setSelectedItem(new Proveedor(Integer.parseInt(rut), nombreProveedor));

            //pasamos a la pestaña de mantenimiento
            tabGeneral.setEnabledAt(0, false);
            tabGeneral.setEnabledAt(1, true);
            tabGeneral.setSelectedIndex(1);
            this.accion = "editar";
            btnGuardar.setText("Editar");

        } else {
            this.mensajeError("Seleccion un registro a editar");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        if (txtCalle.getText().trim().length() == 0 || txtCalle.getText().trim().length() > 45) {
            JOptionPane.showMessageDialog(this, "Debes ingresar un nombre de Calle y no debe ser mayor a 45 caracteres, es obligatorio", "Sistema", JOptionPane.WARNING_MESSAGE);
            txtCalle.requestFocus();
            return;
        }

        if (this.cboProveedor.getItemCount() == 0) {
            JOptionPane.showMessageDialog(this, "Selecciona un proveedor o agrega uno", "Sistema", JOptionPane.WARNING_MESSAGE);
            cboProveedor.requestFocus();
            return;
        }

        String numero = txtNumero.getText().trim();
        String ciudad = txtCiudad.getText().trim();
        String comuna = txtComuna.getText().trim();
        if (numero.length() == 0) {
            numero = "-";
        }
        if (ciudad.length() == 0) {
            ciudad = "-";
        }
        if (comuna.length() == 0) {
            comuna = "-";
        }

        String resp;
        Proveedor proveedor = (Proveedor) this.cboProveedor.getSelectedItem();
        if (accion.equals("editar")) {
            //editar
            resp = CONTROL.actualizar(proveedor.getRut(), rutAnterior, txtCalle.getText().trim(), numero, ciudad, comuna);
            if (resp.equals("OK")) {
                this.mensajeOk("Actualizado correctamente");
                this.limpiar();
                this.listar();

                tabGeneral.setEnabledAt(0, true);
                tabGeneral.setEnabledAt(1, false);
                tabGeneral.setSelectedIndex(0);

            } else {
                this.mensajeError(resp);
            }
        } else {
            //guardar
            resp = CONTROL.insertar(proveedor.getRut(), txtCalle.getText().trim(), numero, ciudad, comuna);
            if (resp.equals("OK")) {
                this.mensajeOk("Registrado correctamente");
                this.limpiar();
                this.listar();

                tabGeneral.setEnabledAt(0, true);
                tabGeneral.setEnabledAt(1, false);
                tabGeneral.setSelectedIndex(0);

            } else {
                this.mensajeError(resp);
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

