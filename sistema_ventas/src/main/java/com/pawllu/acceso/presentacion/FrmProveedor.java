    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (tabla.getSelectedRowCount() == 1) {//boton editar

            //carga la información de la tabla a los campos de texto
            String rut = String.valueOf(tabla.getValueAt(tabla.getSelectedRow(), 0));
            String nombre = String.valueOf(tabla.getValueAt(tabla.getSelectedRow(), 1));
            nombreAnterior = nombre;
            String telefono = String.valueOf(tabla.getValueAt(tabla.getSelectedRow(), 2));
            String paginaWeb = String.valueOf(tabla.getValueAt(tabla.getSelectedRow(), 3));

            txtRut.setText(rut);
            txtNombre.setText(nombre);
            txtTelefono.setText(telefono);
            txtWeb.setText(paginaWeb);

            //pasamos a la pestaña de mantenimiento
            tabGeneral.setEnabledAt(0, false);
            tabGeneral.setEnabledAt(1, true);
            tabGeneral.setSelectedIndex(1);
            this.accion = "editar";
            btnGuardar.setText("Editar");
            txtRut.setEditable(false);
        } else {
            this.mensajeError("Seleccion un registro a editar");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        if (txtRut.getText().trim().length() == 0 || txtRut.getText().trim().length() > 11) {
            JOptionPane.showMessageDialog(this, "Debes ingresar un RUT y no debe ser mayor a 11 caracteres, es obligatorio", "Sistema", JOptionPane.WARNING_MESSAGE);
            txtRut.requestFocus();
            return;
        }

        if (txtNombre.getText().trim().length() == 0 || txtNombre.getText().trim().length() > 25) {
            JOptionPane.showMessageDialog(this, "Debes ingresar un nombre y no debe ser mayor a 25 caracteres, es obligatorio", "Sistema", JOptionPane.WARNING_MESSAGE);
            txtNombre.requestFocus();
            return;
        }

        String telefono = txtTelefono.getText().trim();
        String paginaWeb = txtWeb.getText().trim();
        if (telefono.length() == 0) {
            telefono = "-";
        }
        if (paginaWeb.length() == 0) {
            paginaWeb = "-";
        }

        String resp;
        if (accion.equals("editar")) {
            //editar
            resp = CONTROL.actualizar(Integer.parseInt(txtRut.getText().trim()), txtNombre.getText().trim(), nombreAnterior, telefono, paginaWeb);
            if (resp.equals("OK")) {
                this.mensajeOk("Actualizado correctamente");
                this.limpiar();
                this.listar();

                tabGeneral.setEnabledAt(0, true);
                tabGeneral.setEnabledAt(1, false);
                tabGeneral.setSelectedIndex(0);
                txtRut.setEditable(true);

            } else {
                this.mensajeError(resp);
            }
        } else {
            //guardar
            resp = CONTROL.insertar(Integer.parseInt(txtRut.getText().trim()), txtNombre.getText().trim(), telefono, paginaWeb);
            if (resp.equals("OK")) {
                this.mensajeOk("Registrado correctamente");
                this.limpiar();
                this.listar();

                tabGeneral.setEnabledAt(0, true);
                tabGeneral.setEnabledAt(1, false);
                tabGeneral.setSelectedIndex(0);
                txtRut.setEditable(true);

            } else {
                this.mensajeError(resp);
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

